#Arithmetic operators

x = 45
y = 21

print(x + y)
print( x - y)
print( x * y)
print(x / y)
print(x % y) #Modulus

#Comparison Operators
a  = 56
b = 2
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b) #equal to
print(a != b) #Not equal to

